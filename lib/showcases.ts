/**
 * 恒热（Hot Water Expert）案例样板库
 *
 * 这些是工作流模板的「样板输出」——员工选择某个工作流模板时，可以直接看到
 * 一份高质量的真实案例（hand-crafted by domain experts），既能作为参考，
 * 也能作为 Run live 时输出风格的标杆。
 *
 * 每个 showcase 对应一个 workflow template id，包含：
 *  - meta: 业务背景、目标、约束
 *  - nodes: 每个节点的样板输出（Markdown）
 */

export interface ShowcaseNodeSample {
  /** 必须与 workflow template 中的 node id 完全一致 */
  nodeId: string;
  /** 节点标题（与 template 一致即可） */
  label: string;
  /** Markdown 格式的样板输出 */
  output: string;
}

export interface Showcase {
  /** 与 WORKFLOW_TEMPLATES.id 一致 */
  templateId: string;
  /** 业务场景一句话总结 */
  summary: string;
  /** 触发样板时使用的初始输入（initialInput），方便 Run live 时复现 */
  initialInput: string;
  /** 关键背景与约束（业务设定） */
  context: string;
  /** 每个节点的样板输出 */
  nodes: ShowcaseNodeSample[];
}

